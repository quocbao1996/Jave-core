public class ForIndexDemo {
    public static void main(String[] args) {
        // In ra 1 -> 10
        for (int h = 1; h < 10; h++) {
            System.out.println(h);
        }
        // In ra số chắn 1 - >10
        for (int i = 2; i <= 10; i+=2){
            System.out.println(i);
        }
        // In ra từ  10 -> 1
        for (int f = 10; f >= 1 ; f--) {
            System.out.println(f);

        }
    }
}
